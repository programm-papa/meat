                <div class="text">
                    Наши животные обитают в полной свободе, они могут с лёгкостью перемещаться по пастбищам (общая площадь пастбищ более 1200 га). Сотрудники ежедневно поддерживают комфортные условия и чистоту в любое время года.<br>
                    <br>
                    Вода играет огромную роль в поддержании хорошего здоровья животных, и поэтому она доставляется из собственной скважины прямо в поля. В отличие от обычной, вода со скважины чистая и безопасная.<br>
                    <br>
                    В открытом доступе на пастбищах всегда есть обычная поваренная соль, которая обогащена магнием, кальцием, селеном и цинком, а также витаминами. Соль выкладывается в виде брикетов в специальные кормушки и коровы слизывают необходимое для себя количество. Кроме соли в кормушки добавляют специальный мел, который является природным источником кальция. Это помогает коровам поддерживать кожу и шерсть в отличном состоянии.<br>
                    <br>
                    Большие пастбища дают ещё один плюс – коровы не успевают разбить всю почву, которая потом превращается в грязь. Перемещаясь с одного участка поля на другой, они всегда гуляют в чистоте.
